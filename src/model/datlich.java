package model;

import java.util.Date;

/**
 *
 * @author Legion 5
 */
public class datlich {
    private String gio;
    private Date ngay;

    public datlich() {
    }

    public datlich(String gio, Date ngay) {
        this.gio = gio;
        this.ngay = ngay;
    }

    public String getGio() {
        return gio;
    }

    public void setGio(String gio) {
        this.gio = gio;
    }

    public Date getNgay() {
        return ngay;
    }

    public void setNgay(Date ngay) {
        this.ngay = ngay;
    }

    
    
}
